package com.lmig.ci.policy.services.codemonkey.vo.dunn;

public class DunnAndBradstreetPhoneNumber {
	public String areaCode;
	public String doNotCallRegistry;
	public String phoneNumber;
	public String countryCode;
	
	public String getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	public String getDoNotCallRegistry() {
		return doNotCallRegistry;
	}
	public void setDoNotCallRegistry(String doNotCallRegistry) {
		this.doNotCallRegistry = doNotCallRegistry;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
}
