package com.lmig.ci.policy.services.codemonkey.vo.dunn;

public class DunnAndBradstreetIncomeAssets {
	public String netIncome;
	public String netIncomeGrowthPercent;
	public String period;
	public String salesGrowthPercent;
	public String assets;
	
	public String getNetIncome() {
		return netIncome;
	}
	public void setNetIncome(String netIncome) {
		this.netIncome = netIncome;
	}
	public String getNetIncomeGrowthPercent() {
		return netIncomeGrowthPercent;
	}
	public void setNetIncomeGrowthPercent(String netIncomeGrowthPercent) {
		this.netIncomeGrowthPercent = netIncomeGrowthPercent;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getSalesGrowthPercent() {
		return salesGrowthPercent;
	}
	public void setSalesGrowthPercent(String salesGrowthPercent) {
		this.salesGrowthPercent = salesGrowthPercent;
	}
	public String getAssets() {
		return assets;
	}
	public void setAssets(String assets) {
		this.assets = assets;
	}
}
